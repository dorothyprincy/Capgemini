package switchcase;

public class Month {
	public static void main(String[] args) {
		int Month = 4;
		switch (Month) {
		case 1:
			System.out.println("January");
			break;
		case 2:
			System.out.println("March");
			break;
		case 3:
			System.out.println("May");
			break;
		case 4:
			System.out.println("July");
			break;
		default:
			System.out.println("November");
		
		}
		
	}

}
